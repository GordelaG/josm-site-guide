function initLaptopMap() {
  if (typeof L === 'undefined') return;
  var mapEl = document.getElementById('laptop-map');
  if (!mapEl) return;

  var laptopMap = L.map('laptop-map', {
    zoomControl: false,
    dragging: false,
    scrollWheelZoom: false,
    doubleClickZoom: false,
    boxZoom: false,
    keyboard: false,
    attributionControl: false
  }).setView([-15.7801, -47.9292], 4);

  L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
    attribution: '&copy; OpenStreetMap',
    subdomains: ['a', 'b', 'c'],
    maxZoom: 19
  }).addTo(laptopMap);

  const OSM_COLORS = {
    apron: '#d5d3db',
    runway: '#b7b5c8',
    taxiway: '#b7b5c8',
    parking: '#F1C40F'
  };

  let airportsData = null;
  let airportCycle = ['SBRJ', 'SBSP', 'SBCT']; // Fallback
  if (window.PROJECT_AIRPORTS) {
    const doneAirports = window.PROJECT_AIRPORTS.filter(ap => ap.status === 'done').map(ap => ap.icao);
    if (doneAirports.length > 0) airportCycle = doneAirports;
  }
  let currentAirportIndex = 0;
  let laptopLayers = [];
  let trafficInterval = null;
  let fadeTimeout = null;

  async function loadData() {
    try {
      const response = await fetch(`data/airports_geometry.json?v=${new Date().getTime()}`);
      airportsData = await response.json();
      cycleAirport();
      setInterval(cycleAirport, 20000); // Change every 20s
    } catch (err) {
      console.error("Failed to load airports_geometry.json", err);
      document.getElementById('laptop-airport-label').innerText = "Erro ao carregar dados";
    }
  }

  function cycleAirport() {
    if (!airportsData) return;
    
    // Clear old
    laptopLayers.forEach(l => laptopMap.removeLayer(l));
    laptopLayers = [];
    if (trafficInterval) clearInterval(trafficInterval);
    
    const icao = airportCycle[currentAirportIndex];
    const features = airportsData[icao];
    
    if (!features || features.length === 0) {
      currentAirportIndex = (currentAirportIndex + 1) % airportCycle.length;
      return cycleAirport(); // Skip if missing
    }
    
    const label = document.getElementById('laptop-airport-label');
    label.style.opacity = 0; // fade out
    
    setTimeout(() => {
      label.innerText = `${icao} - Renderização Dinâmica`;
      label.style.opacity = 1; // fade in
      
      const bounds = [];
      let parkingNodes = [];
      
      // Draw features (Hidden from view, used for logic and bounds)
      features.forEach(f => {
        if (f.type === 'node' && f.aeroway === 'parking_position') {
          parkingNodes.push(f);
        } else if (f.type === 'way') {
          const latlngs = f.coords;
          latlngs.forEach(ll => bounds.push(ll));
        }
      });
      
      if (bounds.length > 0) {
        const leafBounds = L.latLngBounds(bounds);
        const center = leafBounds.getCenter();
        laptopMap.invalidateSize();
        // Calculate the zoom needed to fit bounds, then zoom in 1 level more, capped at 18
        const targetZoom = Math.min(18, laptopMap.getBoundsZoom(leafBounds, false, [0, 0]) + 1);
        laptopMap.flyTo(center, targetZoom, { animate: true, duration: 1.5 });
      }
      
      // Spawn Parked Traffic
      parkingNodes.forEach(p => {
        // 40% chance to spawn a parked plane
        if (Math.random() > 0.6) {
           const icon = L.divIcon({
             html: `<div class="plane-icon" style="transform: rotate(${Math.floor(Math.random() * 360)}deg) scale(0.6);"></div>`,
             className: 'dynamic-plane',
             iconSize: [24, 24],
             iconAnchor: [12, 12]
           });
           const marker = L.marker(p.coords, { icon: icon }).addTo(laptopMap);
           laptopLayers.push(marker);
        }
      });
      
      // Animated Traffic
      let movingPlanes = [];
      
      function spawnMovingPlane(ways, isTakeoff) {
        if (!ways || ways.length === 0) return;
        const way = ways[Math.floor(Math.random() * ways.length)];
        const coords = way.coords;
        if (coords.length < 2) return;
        
        const icon = L.divIcon({
           html: `<div class="plane-icon" style="transform: rotate(0deg) scale(${isTakeoff ? 0.9 : 0.6});"></div>`,
           className: 'dynamic-plane',
           iconSize: [24, 24],
           iconAnchor: [12, 12]
        });
        const marker = L.marker(coords[0], { icon: icon }).addTo(laptopMap);
        laptopLayers.push(marker);
        
        movingPlanes.push({
          marker: marker,
          coords: coords,
          currentIndex: 0,
          progress: 0,
          speed: isTakeoff ? 0.04 : 0.015 // takeoff is faster
        });
      }
      
      const runwayWays = features.filter(f => f.type === 'way' && f.aeroway === 'runway');
      const taxiwayWays = features.filter(f => f.type === 'way' && f.aeroway === 'taxiway');
      
      spawnMovingPlane(runwayWays, true); // 1 taking off
      spawnMovingPlane(taxiwayWays, false); // 2 taxiing
      spawnMovingPlane(taxiwayWays, false);
      
      function animateTraffic() {
        movingPlanes.forEach(plane => {
          if (plane.currentIndex >= plane.coords.length - 1) {
            // Loop or restart path
            plane.currentIndex = 0;
            plane.progress = 0;
            plane.marker.setLatLng(plane.coords[0]);
            return;
          }
          
          plane.progress += plane.speed;
          if (plane.progress >= 1) {
            plane.progress = 0;
            plane.currentIndex++;
            if (plane.currentIndex >= plane.coords.length - 1) return;
          }
          
          const p1 = plane.coords[plane.currentIndex];
          const p2 = plane.coords[plane.currentIndex + 1];
          
          const lat = p1[0] + (p2[0] - p1[0]) * plane.progress;
          const lng = p1[1] + (p2[1] - p1[1]) * plane.progress;
          plane.marker.setLatLng([lat, lng]);
          
          // Calculate angle for rotation
          const dy = p2[0] - p1[0];
          const dx = p2[1] - p1[1];
          let angle = Math.atan2(dx, dy) * (180 / Math.PI); // Leaflet/Map orientation
          // Adjust for SVG initial rotation if necessary
          const iconEl = plane.marker.getElement();
          if (iconEl) {
             const inner = iconEl.querySelector('.plane-icon');
             if (inner) {
               inner.style.transform = `rotate(${angle}deg) scale(${plane.speed > 0.03 ? 0.9 : 0.6})`;
             }
          }
        });
      }
      
      trafficInterval = setInterval(animateTraffic, 50); // 20 fps smooth
      
      currentAirportIndex = (currentAirportIndex + 1) % airportCycle.length;
    }, 500); // delay to allow fade
  }

  loadData();
}

initLaptopMap();

